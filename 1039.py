# 문제
# 1039 : [기초-산술연산] 정수 두 개 입력받아 합 출력하기2
# 시간 제한: 1 Sec  메모리 제한: 128 MB
# 제출: 52945  해결 문제 수: 33093

# 문제 설명    
# 정수 2개를 입력받아 합을 출력하는 프로그램을 작성해보자.
# (단, 입력되는 정수는 −2147483648∼2147483647 이다.)

# 입력
# 두 개의 정수가 공백으로 구분되어 입력된다.

# 출력
# 입력 받은 두 정수의 합을 출력한다.

# 입력 예시   
# 2147483647 2147483647

# 출력 예시
# 4294967294

# 코드
a,b=input().split()
print(int(a)+int(b))

